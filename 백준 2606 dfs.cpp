#include <iostream>
#include<vector>

using namespace std;

int N, M = 0;
vector<int>v[101];
bool visited[101];
int cnt = 0;

void Dfs(int _start)
{
	visited[_start] = true;

	for (int i = 0; i < v[_start].size(); i++)
	{
		int x = v[_start][i];
		if (!visited[x])
		{
			Dfs(x);
			cnt++;
		}
	}
}

int main()
{
	cin >> N >> M;

	for (int i = 0; i < M; i++)
	{
		int input_1, input_2 = 0;

		cin >> input_1 >> input_2;
		v[input_1].push_back(input_2);
		v[input_2].push_back(input_1);
	}
	Dfs(1);
	cout << cnt;
	return 0;
}


































































































































































































































































































































































