#include <iostream>
#include <queue>
#include <algorithm>

using namespace std;

int main()
{
	ios::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	priority_queue<int, vector<int>, greater<int>> q;

	int N = 0;
	cin >> N;

	while (N--)
	{
		int input = 0;
		cin >> input;
		if (input == 0)
		{
			if (q.empty())
			{
				cout << "0";
			}
			else
			{
				cout << q.top();
				q.pop();
			}
		}
		else
		{
			q.push(input);
		}
	}
	return 0;
}






































































































































































































































































































































































