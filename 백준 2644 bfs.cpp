#include <iostream>
#include <queue>

using namespace std;

int n, target_x, target_y, m;
int cnt = 0;
int arr[102][102];
int visited[102];
queue<int> q;

void bfs(int _x) {
    q.push(_x);

    while (!q.empty()) 
    {
        _x = q.front();
        q.pop();
        for (auto i = 1; i <= n; i++) {
            if (arr[_x][i] != 0 && !visited[i]) {
                q.push(i);
                visited[i] = visited[_x] + 1;
            }
        }
    }
}

int main() {
    cin >> n;
    cin >> target_x >> target_y;
    cin >> m;

    for (int i = 0; i < m; i++)
    {
        int x, y = 0;
        cin >> x >> y;

        arr[x][y] = 1;
        arr[y][x] = 1;
    }

    bfs(target_x);

    if (visited[target_y] == 0)
        visited[target_y] = -1;

    cout << visited[target_y];
}
































































































































































































































































































































































